package entidad;

public class AsignaturaNota {
	
	private int idPruebaNotas;
	
	private String codPrueba;
	
	private String codGrupo;
	
	private int idMatricula;
	
	private int nota;
	
	private int numeroPrueba;
	
	private double peso;

	public int getIdPruebaNotas() {
		return idPruebaNotas;
	}

	public void setIdPruebaNotas(int idPruebaNotas) {
		this.idPruebaNotas = idPruebaNotas;
	}

	public String getCodPrueba() {
		return codPrueba;
	}

	public void setCodPrueba(String codPrueba) {
		this.codPrueba = codPrueba;
	}

	public String getCodGrupo() {
		return codGrupo;
	}

	public void setCodGrupo(String codGrupo) {
		this.codGrupo = codGrupo;
	}

	public int getIdMatricula() {
		return idMatricula;
	}

	public void setIdMatricula(int idMatricula) {
		this.idMatricula = idMatricula;
	}

	public int getNota() {
		return nota;
	}

	public void setNota(int nota) {
		this.nota = nota;
	}

	public int getNumeroPrueba() {
		return numeroPrueba;
	}

	public void setNumeroPrueba(int numeroPrueba) {
		this.numeroPrueba = numeroPrueba;
	}

	public double getPeso() {
		return peso;
	}

	public void setPeso(double peso) {
		this.peso = peso;
	}
	
	

}
