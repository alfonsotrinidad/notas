package entidad;

public class AsignaturaDetalle {
	
	private int idAsingDetalle;
	
	private String idSeccion;
	
	private String idAsign;
	
	private String codModalidad;

	public int getIdAsingDetalle() {
		return idAsingDetalle;
	}

	public void setIdAsingDetalle(int idAsingDetalle) {
		this.idAsingDetalle = idAsingDetalle;
	}

	public String getIdSeccion() {
		return idSeccion;
	}

	public void setIdSeccion(String idSeccion) {
		this.idSeccion = idSeccion;
	}

	public String getIdAsign() {
		return idAsign;
	}

	public void setIdAsign(String idAsign) {
		this.idAsign = idAsign;
	}

	public String getCodModalidad() {
		return codModalidad;
	}

	public void setCodModalidad(String codModalidad) {
		this.codModalidad = codModalidad;
	}
	
	

}
