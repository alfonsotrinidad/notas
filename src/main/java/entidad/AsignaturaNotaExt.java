package entidad;

public class AsignaturaNotaExt extends AsignaturaNota{
	
	private String idAsign;
	
	private String descripAsign;
	
	private String codPrueba;
	
	private String descripPrueba;

	public String getIdAsign() {
		return idAsign;
	}

	public void setIdAsign(String idAsign) {
		this.idAsign = idAsign;
	}

	public String getDescripAsign() {
		return descripAsign;
	}

	public void setDescripAsign(String descripAsign) {
		this.descripAsign = descripAsign;
	}

	public String getCodPrueba() {
		return codPrueba;
	}

	public void setCodPrueba(String codPrueba) {
		this.codPrueba = codPrueba;
	}

	public String getDescripPrueba() {
		return descripPrueba;
	}

	public void setDescripPrueba(String descripPrueba) {
		this.descripPrueba = descripPrueba;
	}
	
	

}
